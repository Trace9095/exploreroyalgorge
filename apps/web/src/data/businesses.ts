// ─── Helpers ──────────────────────────────────────────────────────────────

export function getBusinessBySlug(slug: string): Business | undefined {
  return BUSINESSES.find((b) => b.slug === slug)
}

export function getBusinessesByCategory(category: string): Business[] {
  return BUSINESSES.filter((b) => b.category === category)
}

export function getFeaturedBusinesses(): Business[] {
  return BUSINESSES.filter((b) => b.featured).sort((a, b) => {
    const tierOrder = { sponsored: 0, premium: 1, free: 2 }
    return tierOrder[a.tier] - tierOrder[b.tier]
  })
}

export function getUnclaimedBusinesses(): Business[] {
  return BUSINESSES.filter((b) => !b.claimed)
}

export function getOwnedBusinesses(): Business[] {
  return BUSINESSES.filter((b) => b.ownedByTrace)
}
